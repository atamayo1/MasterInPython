
"""

Ejercicio 1:
     - Crear variables una "pais" y otra "continente"
     - Mostrar su valor por pantalla (imprimir)
     - Poner un comentario diciendo el tipo de dato
"""

pais = input("Ingrese el pais: ")
continente = input("Ingrese el continente: ")
year = int(input("Ingrese el año actual: "))

print(f"El tipo de dato del país {pais} es: {type(pais)}")
print(f"El tipo de dato del continente {continente} es: {type(continente)}")
print(f"El tipo de dato del año {str(year)} es: {type(year)}")
