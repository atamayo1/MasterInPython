
# Operadores aritméticos
num1 = 77
num2 = 44 # operador asignación =

# plus
suma = num1 + num2
# minus
resta = num1 - num2
# multiplication
multiplicacion = num1 * num2
# division
division = num1 / num2
# residuo
residuo = num1 % num2

print("**************** CALCULADORA ******************")
print(f"La suma de {num1} mas {num2} es : {suma}")
print(f"La resta de {num1} menos {num2} es : {resta}")
print(f"La multiplicación de {num1} por {num2} es : {multiplicacion}")
print(f"La división entre {num1} dividido {num2} es : {division}")
print(f"El residuo entre {num1} y {num2} es : {residuo}")
